package com.ZohoQns;

public class Qn34 {
    public static void main(String[] args) {
        int[] arr = {1, 7, 2, 6, 3, 5,4};
        for(int i = 0;i<arr.length;i++){
            System.out.println((i+1)+" element is "+arr[i]);
        }
    }
}
