//Write a program to print a letters from 'A' to the user input character without using strings.
//input : D
//output : ABCD
package com.ZohoQns;

public class Qn47 {
    public static void main(String[] args) {
        char end = 'D';
        char start = 'A';
        while(start<=end){
            if(start==end){
                System.out.print(start);
            }
            else {
                System.out.print(start);
            }
            start++;
        }
        System.out.println();
    }
}
